# Создайте новую среду под названием myenv.
# conda create --name myenv

# Активируйте среду myenv.
# conda activate myenv

# Установите пакет numpy в среду myenv.
# conda install numpy

# Отобразите список всех установленных пакетов в среде myenv.
# conda list

# Деактивируйте среду myenv.
# conda deactivate
