// В JavaScript есть 8 основных типов данных

// Число (number)
alert( 1 / 0 ); // Infinity
alert(typeof 5); // number

// BigInt (bigint)
alert(9007199254740991 + 1); // 9007199254740992

// Строка (string)
alert(typeof "Привет"); // string

// Булевый тип (boolean)
alert(typeof false); // boolean

// null
alert(null); // null

// undefined
let age;
alert(age); // выведет "undefined"

// Cимволы (symbol)

// Объекты
console.log(typeof null); // "object"
